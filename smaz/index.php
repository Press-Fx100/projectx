                <p><b>Soalan Lazim Agihan Zakat</b><br><hr>
Siapakah yang boleh memohon bantuan zakat Baitulmal MAIWP?<br>
<br>
Sesiapa sahaja yang tergolong dalam golongan fakir, miskin, amil,muallaf, al-gharimin (orang yang berhutang), ibnu sabil, fisabilillah dan ar-riqab. Antaranya orang miskin, pelajar sekolah dan pengajian tinggi, mereka yang ditimpa penyakit, peniaga kecil, pemandu teksi dan lain-lain lagi.<br>
<br><hr>
Apakah syarat-syarat bagi memohon bantuan zakat Baitulmal MAIWP?<br>
<br>
Terdapat dua syarat iaitu syarat umum dan khusu. Syarat umum adalah Islam, warganegara Malaysia, menetap di Wilayah Persekutuan sekurang-kurangnya setahun dan bergantungkepada pengiraan keperluan asasi dan pendapatan isi rumah (Had al-Kifayah) Syarat khusus pula adalah bergantung kepada bantuan yang dipohon dan berbeza-beza mengikut skim bantuan.<br>
<br><hr>
Bagaimanakah caranya untuk memohon bantuan zakat daripada Baitulmal MAIWP?<br>
<br>
Datang sendiri ke pejabat Baitulmal MAIWP atau boleh melayari laman web www.maiwp.gov.my untuk maklumat lanjut mengenai permohonan bantuan dan memuat turun borang permohonan.<br>
<br><hr>
Berapakah jumlah bantuan zakat yang diberikan?<br>
<br>
Jumlah bantuan zakat yang diberikan kepada seorang pemohon yang layak adalah berbeza-beza mengikut jenis bantuan dan taraf kehidupan pemohon.<br>
<br><hr>
Bolehkah saya memohon lebih daripada satu bantuan?<br>
<br>
Pemohon boleh memohon lebih daripada satu bantuan setelah pihak Baitulmal MAIWP mengenalpasti kelayakan pemohon.<br>
<br><hr>
Adakah bantuan ini perlu dibayar semula?<br>
<br>
Tidak, semua bantuan yang diberikan bukan berbentuk pinjaman dan tidak perlu dibayar semula<br></p>
